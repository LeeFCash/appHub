package org.example.app;

public class ContactMeResponse {

    public String name;
    public String email;
    public String phoneNumber;
    public String profile;

public ContactMeResponse( String name, String email, String phoneNumber, String profile) {
    this.name = name;
    this.email = email;
    this.phoneNumber = phoneNumber;
    this.profile = profile;
    }
}

